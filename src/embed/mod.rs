/// Enhance CSS for SVG theme adaptation and dark mode.
pub const ENHANCE_CSS: EmbeddedAsset = EmbeddedAsset::new(
    AssetKind::Css,
    "enhance",
    include_str!("css/enhance.css"),
);

/// Hot reload runtime JavaScript.
pub const HOTRELOAD_JS: EmbeddedAsset = EmbeddedAsset::new(
    AssetKind::JavaScript,
    "hotreload",
    include_str!("serve/hotreload.js"),
);

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_asset_filename() {
        let filename = ENHANCE_CSS.filename();
        assert!(filename.starts_with(".enhance-"));
        assert!(filename.ends_with(".css"));
    }

    #[test]
    fn test_asset_hash_length() {
        let hash = ENHANCE_CSS.hash();
        assert_eq!(hash.len(), 8);
        assert!(hash.chars().all(|c| c.is_ascii_hexdigit()));
    }

    #[test]
    fn test_template_render() {
        let js = EmbeddedAsset::new(
            AssetKind::JavaScript,
            "test",
            "connect(__TOLA_WS_PORT__)",
        );
        let rendered = js.render(&[TemplateVar::WsPort(35729)]);
        assert_eq!(rendered, "connect(35729)");
    }

    #[test]
    fn test_html_tag_css() {
        let tag = ENHANCE_CSS.html_tag();
        assert!(tag.starts_with(r#"<link rel="stylesheet" href="/.enhance-"#));
        assert!(tag.contains("data-tola-generated"));
        assert!(tag.ends_with(r#">"#));
    }

    #[test]
    fn test_html_tag_js() {
        let tag = HOTRELOAD_JS.html_tag();
        assert!(tag.starts_with(r#"<script src="/.hotreload-"#));
        assert!(tag.contains("data-tola-generated"));
        assert!(tag.ends_with(r#"></script>"#));
    }
}
